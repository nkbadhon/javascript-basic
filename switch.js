const color = 'red';
switch (color) {
    case 'white':
        console.log('The color is White');
        break;
    case 'blue':
        console.log('The color is Blue');
        break;
    case 'red':
        console.log('The color is Red');
        break;
    case 'yellow':
        console.log('The color is Yellow');
        break;
    case 'green':
        console.log('The color is Green');
        break;

    default:
        console.log('No color');

}